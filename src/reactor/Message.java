package reactor;

public class Message {

	private int type_;
	public String msg_;
	
	public int getType()
	{
		return type_;
	}
	
	public void setType(int type_)
	{
		this.type_ = type_;
	}
	
	public String getMsg()
	{
		return msg_;
	}
	
	public void setMsg(String msg_)
	{
		this.msg_ = msg_;
	}
	
}
